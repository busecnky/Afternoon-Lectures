public class Question49 {
	
	public static void main(String[] args) {
		
		// 1 1 1
		// 2 2 2
		// 3 3 3
		
		int [][] matris1 = new int [3][3];
		
		for (int i = 0; i < matris1.length; i++) {
			for (int j = 0; j < matris1[i].length; j++) {
				matris1[i][j] = i+1;
				System.out.print(matris1[i][j] + " ");
			}
			System.out.println();
		}
		
				
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	}

}
